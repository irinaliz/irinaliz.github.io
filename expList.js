function getMonsterParkExp(lvl, sunday, event) {
    var exp, plusExp = 0;
    if (lvl >= 200 && lvl <= 209) {
        exp = 179957540;
    }
    if (lvl >= 210 && lvl <= 219) {
        exp = 642539340;
    }
    if (lvl >= 220 && lvl <= 224) {
        exp = 1237561920;
    }
    if (lvl >= 225 && lvl <= 229) {
        exp = 2241701605;
    }
    if (lvl >= 230 && lvl <= 234) {
        exp = 2724323200;
    }
    if (lvl >= 235 && lvl <= 239) {
        exp = 3145303350;
    }
    if (lvl >= 240 && lvl <= 244) {
        exp = 3630339550;
    }
    if (lvl >= 245) {
        exp = 4686646600;
    }
    plusExp = 0 //- exp;
    if (sunday == 1) {
        plusExp += Math.floor(exp * 0.5);
    }
    if (event == 1) {
        plusExp += Math.floor(exp * 0.5);
    }
    return {'exp':exp, 'plusExp': plusExp};
}
